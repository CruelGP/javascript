var cont = 0
//Incrementa a variável uma unidade.
cont++;
console.log(cont)
//Decrementa a variável uma unidade
cont--;
console.log(cont)
//Soma 2: o mesmo que cont = cont + 2;
cont += 2;
console.log(cont)

//Multiplica por 3: o mesmo que cont = cont *3; (nesta operação o cont tem o valor de 2.)
cont *= 3;
console.log(cont)
